#include <stdio.h>

int main() {
    printf("Hello, World!\n");
    printf("%d\n", 100);       // %d= place holder
    printf("%ld\n",41563856); //%ld= large integer
    printf("%f\n", 100.2);     //%f= float/decimal
    printf("%lf\n",4156.38567); //%ld= large float
    printf("%c\n", 's');        //%c= character
    printf("%s\n", "This is my program"); //%s= string
    return 0;

}